package diana.padilla.poodiana

class Calculadora {

    fun sumar(num1: Int, num2: Int): Int{
        val resultado = num1+num2

        return resultado
    }

    fun restar(num1: Int, num2: Int): Int{
        val resultado = num1-num2

        return resultado
    }

    fun multiplicar(num1: Int, num2: Int): Int{
        val resultado = num1*num2

        return resultado
    }

    fun dividir(num1: Int, num2: Int): Int{
        val resultado = num1/num2

        return resultado
    }
}